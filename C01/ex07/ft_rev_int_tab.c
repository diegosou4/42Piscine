void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	temp;

	j = size;
	i = 0;
	while (i < (size / 2))
	{
		temp = tab[j - 1];
		tab[j - 1] = tab[i];
		tab[i] = temp;
		i++;
		j--;
	}
}
