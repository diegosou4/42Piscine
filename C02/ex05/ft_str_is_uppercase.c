int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	if (str[0] == '\0')
	{
		i = 1;
	}
	while (str[j] != '\0')
	{
		if (str[j] >= 65 && str[j] <= 90)
		{
			i = 1;
		}
		else
		{
			return (0);
		}
		j++;
	}
	return (i);
}
